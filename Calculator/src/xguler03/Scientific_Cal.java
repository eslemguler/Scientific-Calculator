    public double Evaluation(final String str) {
        return new Object() {
            int pos = -1, ch;

            void nextChar() {

                ch = (++pos < str.length()) ? str.charAt(pos) : -1;
            }

            boolean sign(int charToSign) {
                while (ch == ' ') nextChar();
                if (ch == charToSign) {
                    nextChar();
                    return true;
                }
                return false;
            }

            double parse() {
                nextChar();
                double x = parseSign();
                if (pos < str.length()){
                    throw new RuntimeException("Unexpected: " + (char)ch);
                }
                return x;
            }

            double parseSign() {
                double x = parseCarp();
                while(true) {
                    if      (sign('+')) x += parseCarp(); // addition
                    else if (sign('-')) x -= parseCarp(); // subtraction
                    else return x;
                }
            }

            double parseCarp() {
                double x = parseCheck();
                while(true) {
                    if      (sign('*')) x *= parseCheck(); // multiplication
                    else if (sign('/')) x /= parseCheck(); // division
                    else return x;
                }
            }

            double parseCheck() {
                double x = 0;
                int startPos = this.pos;
                if (sign('(')) { // parentheses
                    x = parseSign();
                    sign(')');
                } 
                else if ((ch >= '0' && ch <= '9') || ch == '.') { // numbers
                    while ((ch >= '0' && ch <= '9') || ch == '.') nextChar(); 
                    
                    if(base ==1){
                        x = Integer.parseInt(str.substring(startPos, this.pos),2);
                    }
                    
                    else{
                        x = Double.parseDouble(str.substring(startPos, this.pos));
                    }  
                } 
                
                else if (ch >= '0' && ch <= 'f' && base==2){
                    
                    while ((ch >= '0' && ch <= 'f' && base==2)) nextChar();
                    x = Integer.parseInt(str.substring(startPos, this.pos),16);
                }
                
                else if (ch >= 'a' && ch <= 'z' ) { // functions
                    while (ch >= 'a' && ch <= 'z') nextChar();
                    String func = str.substring(startPos, this.pos);
                    x = parseCheck();
                    
                    if (func.equals("sin")) x = Math.sin(Math.toRadians(x));
                    else if (func.equals("cos")) x = Math.cos(Math.toRadians(x));
                    else if (func.equals("log")) x = Math.log(x) / Math.log(2);
              
                }

                if (sign('^')) x = Math.pow(x, parseCheck()); // exponentiation
               
                return x;
                
            }
        }.parse();
    }

